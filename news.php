                            <h2>Basmi Pencurian Ikan, Kini RI Jadi Eksportir Terbesar ke Jepang</h2>
                            <hr class="star-primary">
                            <img src="img/news/1.jpg" class="img-responsive img-centered" alt="">
                            <p><em>Lima kapal asing pencuri ikan yang ditangkap petugas Pangkalan Pengawasan Sumber Daya Kelautan dan Perikanan (PSDKP) Bitung, Sulawesi Utara. (Liputan6.com/Yoseph Ikanubun)</em></p>
                            <br>
                            <p class="text-justify">Keinginan pemerintah melalui Kementerian Kelautan dan Perikanan (KKP) untuk membasmi pencurian ikan membuahkan hasil. Dengan menindak tegas pencuri ikan, hasil tangkapan ikan nelayan lokal terus meningkat sejalan dengan kenaikan ekspor. Menteri Kelautan dan Perikanan (MKP) Susi Pudjiastuti mengatakan, pemberantasan pencurian ikan telah mendorong Indonesia menjadi eksportir tuna terbesar ke Jepang. Terpenting, ikan tuna tersebut dipastikan merupakan murni hasil tangkapan nelayan asli Indonesia.</p>
                            <p class="text-justify">"Recovery dalam 1,5 tahun mulai kelihatan. Hasil tangkapan nelayan banyak. Nilai tangkapan dan nilai ekspor sudah jauh meningkat. Kita nomor satu untuk tuna ekspor ke Jepang. Sudah murni 1,5 tahun didapat kekuatan domestik karena kapal eks asing diselesaikan dengan moratorium," ‎kata Susi di rumah dinasnya, Jakarta, Kamis (4/8/2016). Dia menerangkan, selama 20 tahun terakhir perikanan nasional mengalami penurunan yang cukup signifikan. Penyebabnya, penangkapan ikan ilegal oleh asing yang tidak terkontrol.</p>
                            <p class="text-justify">"‎Itu  adalah akibat dari sumber daya laut yang jauh menurun karena sumber daya kita rusak karena penangkapan ikan yang sudah berlebihan dilakukan oleh ribuan kapal besar yang izinnya 1.300 GT. Namun di laut kapalnya 10 kali lipat lebih. Dari situ saya melihat kita tidak boleh setengah-setengah dalam memerangi illegal fishing," ungkap dia. Susi kemudian menceritakan pengalaman saat menjadi pengusaha ikan. Di sekitar tahun 1985, untuk mencari ikan sebanyak 10 ton sehari ialah perkara mudah. Namun, hal tersebut tidak terjadi pada saat ini.</p>
                            <p class="text-justify">"Cirebon dulu semalam, saya masih ingat tahun 1985 meminjam truk dari pabrik untuk bisa angkut dari Gebang, Cirebon, Indramayu bisa 10 ton. Sekarang 1 ton aja susah," ujar dia. Tak sekadar itu, penangkapan ikan yang tidak terkontrol juga membuat nelayan akhirnya kehilangan pekerjaan. Hal tersebut karena pekerjaan menjadi nelayan dianggap tidak menguntungkan. "Sementara laut kita dijarah oleh puluhan ribu kapal yang akhirnya profesi nelayan tidak menarik. Jumlah rumah tangga nelayan dari 2003 sampai 2013 turun 1,6 juta jadi 800 rumah tangga nelayan," tutup dia.(Amd/Nrm)</p>
