                            <h2>2. Operasi Penangkapan Ikan (OPI)</h2>
                            <hr class="star-primary">
                            <p class="text-justify">Setelah persiapan selesai dilakukan, seluruh nelayan dalam sebuah kapal yang terdiri dari nahkoda dan ABK meninggalkan pangkalan pendaratan ikan menuju daerah penangkapan ikan (fishing ground).  Waktu yang dibutuhkan sebuah kapal dari fishing base menuju fishing ground bervariasi  mulai dari 2 jam hingga 15 hari.</p> 
                            <p class="text-justify">ABK kapal mulai melakukan penangkapan ikan ketika mendapatkan perintah dari nahkoda. Rata – rata waktu kerja nelayan selama satu hari adalah 12 jam. Waktu kerja ini berbeda antara satu kapal dengan kapal lainnya. ABK di beberapa daerah melakukan pembagian jam kerja (shifting) namun ABK di daerah lainnya melakukan aktivitas penangkapan ikan ini secara bersama-sama.</p>
                            <p class="text-justify">Ikan yang berhasil ditangkap lalu dimasukkan ke dalam palka yang berisi es/freon untuk mencegah penurunan mutu ikan segar. Lama perjalanan untuk setiap penangkapan ikan berbeda – beda mulai dari satu hari (one day fishing) hingga lima bulan. Hal ini dipengaruhi oleh perbekalan, ukuran perahu/kapal, dan cuaca.</p>    
