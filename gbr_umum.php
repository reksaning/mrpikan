                            <h2>3. Pendaratan Ikan di TPI</h2>
                            <hr class="star-primary">
                            <p class="text-justify">Setelah melakukan perjalanan penangkapan ikan, kapal kemudian kembali menuju ke fishing base. Ikan hasil tangkapan didalam palka kapal dibongkar oleh ABK dan kemudian disortir berdasarkan jenis dan kelas mutunya. Setelah disortir, ikan akan dilaporkan dan dibawa ke TPI untuk diikutsertakan dalam pelelangan ikan. Proses penurunan ikan dari atas kapal menuju ke TPI dilakukan oleh pemikul bayaran.</p>   
