                            <h2>4. Distribusi</h2>
                            <hr class="star-primary">
                            <p class="text-justify">Setelah melalui proses transaksi jual beli di tempat pelelangan ikan, ikan segar kemudian diangkut menuju kendaraan oleh pemikul. Upah pemikul untuk proses ini dibebankan kepada pihak yang membeli ikan. Ikan akan didistribusikan ke pabrik, unit pengolahan ikan (UPI), pasar tradisional, supermarket (pasar modern), atau di ekspor ke luar negeri. Ikan ditempatkan dalam suatu wadah berupa styrofoam, blong, atau cold box dan ditambahkan dengan es kemudian disusun rapi di atas kendaraan. Daerah distribusi yang dituju tergantung dari jenis ikannya. Ikan sejenis tuna atau swanggi yang bernilai ekonomi tinggi dan diperuntukkan untuk tujuan ekspor biasa didistribusikan ke Jakarta atau Surabaya.</p>   
