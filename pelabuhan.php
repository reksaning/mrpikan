                            <p class="text-justify">Pelabuhan Perikanan Nusantara Brondong adalah salah satu pelabuhan perikanan nusantara yang ada di Jawa Timur.  Sebagai Unit Pelaksana Teknis Direktorat Jenderal Perikanan Tangkap yang bertanggungjawab kepada Kementerian Kelautan dan Perikanan (Peraturan Menteri Kelautan dan Perikanan No.PER 06/MEN/2007 Tanggal 25 Januari 2007 tentang Organisasi dan Tata Kerja Pelabuhan Perikanan), menjalankan aktivitas bongkar muat dan pemasaran hasil perikanan rata-rata 30 - 150 ton per hari.</p>
                            <p class="text-justify">Lokasi Pelabuhan Perikanan Nusantara Brondong terletak di Kel. Brondong, Kec. Brondong, Kab. Lamongan, Jawa Timur dengan posisi koordinat secara geografis pada 06o 53’ 30, 81” LS dan 112o 17’ 01, 22” BT. Fungsi PPN Brondong, sesuai dengan Undang-undang No. 45 Tahun 2009 tentang perikanan, adalah mendukung kegiatan yang berhubungan dengan pengelolaan dan pemanfaatan sumber daya ikan dan lingkungannya antara lain :
                            </p>                    
                            
